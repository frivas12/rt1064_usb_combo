lock_guard::lock_guard(const SemaphoreHandle_t handle) :
    _handle(handle)
{
    xSemaphoreTake(_handle, portMAX_DELAY);
}

lock_guard::~lock_guard()
{
    xSemaphoreGive(_handle);
}


