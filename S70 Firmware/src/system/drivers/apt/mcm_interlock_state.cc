drivers::usb::apt_response_builder &
mcm_interlock_state::get(drivers::usb::apt_response_builder &builder,
                         bool interlock_state) {
    return builder.command(COMMAND_GET)
        .parameters(drivers::usb::apt_response::parameters{
            .param1 = static_cast<uint8_t>(interlock_state ? 1 : 0),
            .param2 = 0,
        });
}

