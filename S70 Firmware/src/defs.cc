slot_types operator++ (slot_types& base)
{
    base = static_cast<slot_types>(static_cast<uint16_t>(base) + 1);
    return base;
}

slot_types operator-- (slot_types& base)
{
    base = static_cast<slot_types>(static_cast<uint16_t>(base) - 1);
    return base;
}

slot_types operator+= (slot_types& base, const slot_types offset)
{
    base = static_cast<slot_types>(static_cast<uint16_t>(base) + static_cast<uint16_t>(offset));
    return base;
}

slot_types operator-= (slot_types& base, const slot_types offset)
{
    base = static_cast<slot_types>(static_cast<uint16_t>(base) - static_cast<uint16_t>(offset));
    return base;
}

bool operator== (config_signature_t& x, config_signature_t& y)
{
    return x.config_id == y.config_id && x.struct_id == y.struct_id;
}

